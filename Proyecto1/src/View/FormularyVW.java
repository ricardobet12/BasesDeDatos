        public void leer(){
        try {
            FileReader entrada = new FileReader("DbTexto.txt");
            BufferedReader leer = new BufferedReader(entrada);
            String linea = leer.readLine();
            while(linea!=null){
                areaText.append(linea+"\n");
                linea=leer.readLine();
            }
         
        } catch (IOException ex) {
            System.out.println(ex);
        }
    }

    private void btnExportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExportActionPerformed
        if (ptexto.getSelectedIndex() == 0) {
            try {
                JFileChooser archivo = new JFileChooser(System.getProperty("user.dir"));
                archivo.showSaveDialog(this);
                if (archivo.getSelectedFile() != null) {
                    try (FileWriter guardado = new FileWriter(archivo.getSelectedFile())) {
                        guardado.write(areaText.getText());
                        JOptionPane.showMessageDialog(rootPane, "El archivo  de Texto fue guardado con éxito en la ruta establecida");
                    }
                }

            } catch (IOException ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage());
            }
        }

        if (ptexto.getSelectedIndex() == 1) {
            try {
                JFileChooser archivo = new JFileChooser(System.getProperty("user.dir"));
                archivo.showSaveDialog(this);
                if (archivo.getSelectedFile() != null) {
                    try (FileWriter guardado = new FileWriter(archivo.getSelectedFile())) {
                        guardado.write(areaTextBinary.getText());
                        JOptionPane.showMessageDialog(rootPane, "El archivo  de Texto+Binario fue guardado con éxito en la ruta establecida");
                    }

                }

            } catch (IOException ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage());
            }
        }
        if (ptexto.getSelectedIndex() == 2) {
            try {
                JFileChooser archivo = new JFileChooser(System.getProperty("user.dir"));
                archivo.showSaveDialog(this);
                if (archivo.getSelectedFile() != null) {
                    try (FileWriter guardado = new FileWriter(archivo.getSelectedFile())) {
                        guardado.write(textAreaAleatorio.getText());
                        JOptionPane.showMessageDialog(rootPane, "El archivo  aleatorio fue guardado con éxito en la ruta establecida");
                    }

                }

            } catch (IOException ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage());
            }
        }
        

    }//GEN-LAST:event_btnExportActionPerformed

